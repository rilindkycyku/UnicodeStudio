export function toUnicodeStyle(text, styleId = 'sans-bold') {
  if (!text) return "";

  // Special handling for Strikethrough (Combining Overlay)
  if (styleId === 'strikethrough') {
    return text.split('').map(char => char + '\u0336').join('');
  }

  const selectedMapping = mapping[styleId] || mapping['sans-bold'];

  return text.split('').map(char => {
    // Handle Albanian special cases first (combining marks)
    if (char === 'Ã«') return (selectedMapping['e'] || 'e') + '\u0308';
    if (char === 'Ã‹') return (selectedMapping['E'] || 'E') + '\u0308';
    if (char === 'Ã§') return (selectedMapping['c'] || 'c') + '\u0327';
    if (char === 'Ã‡') return (selectedMapping['C'] || 'C') + '\u0327';

    // Normal mapping
    return selectedMapping[char] || char;
  }).join('');
}
